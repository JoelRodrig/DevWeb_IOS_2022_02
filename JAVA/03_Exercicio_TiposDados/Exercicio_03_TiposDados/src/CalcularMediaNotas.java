public class CalcularMediaNotas {
    public static void main(String[] args) {
        int aluno1 = 3;
        int aluno2 = 5;
        int aluno3 = 7;
        int aluno4 = 9;

        int somaNotas = (aluno1 + aluno2 + aluno3 + aluno4) / 4;
        System.out.println("A média do aluno é: " + somaNotas);
    }
}